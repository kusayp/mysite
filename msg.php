        <p>What is the gospel? Simply put, the gospel is the good news about the salvation we can experience when we repent of our sins and place our faith on the death, burial, and resurrection of Jesus Christ.&nbsp; The gospel begins with our view of God.&nbsp; Since God is the Creator of the universe He is the ruler of all creation (Genesis 1:1; Colossians 1:15-20). God, therefore, deserves our worship; in other words, we are accountable to God.&nbsp; Every time we go against God or anytime we break God’s moral law we are guilty of sin.&nbsp; Sin has separated us from God who is holy and righteous.&nbsp; Something had to be done about our sin problem if we were going to be restored into having a right standing with God.&nbsp; That’s where Jesus steps in. Jesus Christ, the Son of God, came to earth born of a virgin to take on the form of man (John 1:14). He lived a perfect, sinless life (Hebrews 4:14-16), and He died on the cross for us.&nbsp; Christ’s atoning death on the cross satisfied the wrath of God toward our sin problem (Romans 6:23; 1 John 2:1-2; 2 Corinthians 5:21).</p>
