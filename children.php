<h3 class="cufon_headings" style="font-size: 14.3px;">Kids D-Groups (Wednesdays in The Underground):</h3>
<p>We are introducing Kid D-Groups using <a href="http://lcm.wol.org/content/children" _blank"="">Word of Life Olympians</a>. This is an award-based curriculum, similar to AWANA. The children will have games, music, Bible lessons and D-Groups each Wednesday night. They are designed to teach children how to be in the Word of God on a consistent basis, learn the discipline of Scripture memory, be active in Christian reading, and serve with their parents in family-lead Christian service. In Kids D-Groups each week, there is a lesson follow-up, discussion about what they learned in their Quiet Time, and review of Scripture memory. </p>
<p>This program is designed with you, the parent, in mind. It is a great tool for family discipleship. Quiet time books are available for all ages of the family. </p>
<p>Throughout the year the child can earn points and rewards for completing faithful quiet time, Scripture memory, etc., with the goal of earning a gold medal each year. Should a child earn a gold medal for 6 consecutive years, there are scholarships available to over 20 Christian Colleges and Universities. </p>
