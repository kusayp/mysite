        <p>Our Purpose:<br>
We want to <strong>Deliver</strong> the Word of God in a creative, fun, and engaging way.<br>
We want to partner with our parents in their child’s <strong>Discipleship</strong> journey.<br>
We want to introduce the children to the idea of being <strong>Deployed</strong> locally and around the world to carry the message of the Gospel. </p>
<p>Our desire at Brainerd is that children would be introduced to a relationship with Jesus Christ through repentance and faith, resulting in becoming lifelong followers of Him. </p>
<p>If you would like to receive monthly email updates, <a title="Kids Email List" href="http://eepurl.com/fuon2" target="_top">sign up</a> or join our <a href="https://www.facebook.com/groups/134615806600668/" target="_top">Facebook group</a>.</p>
<h3 class="cufon_headings" style="font-size: 14.3px;">Kids Weekends (The Underground):</h3>
<p>Our weekends will be designed to reinforce Biblical truths and assist in each child’s walk with the Lord. In our weekend ministry we will utilize  <a href="http://www.gospelproject.com/kids/">The Gospel Project for Kids</a> curriculum. This study will take us on a 3-year chronological journey through the entire story of the Bible. Each week makes a reference point to the Gospel of Jesus Christ.  Through hands-on activities, Bible stories, videos, dramas, and worship music, we will Deliver the Word in an exciting way!  </p>
<p>There are two programs for Kids on the weekend: Kids Life Groups and Kids Church.<br>
Both programs will use the Gospel Project with activities and teaching time to emphasize the lesson. </p>
<p><strong>Kids Life Groups:</strong><br>
In this unique setting and through the engaging teaching methods, our children learn biblical concepts and how to apply them to their everyday lives. Classrooms have been redesigned into creative, media-inspired, kid-pleasing rooms that enhance the teaching of Biblical truths. Everything we do in Life Groups is focused on Scripture with the purpose of leading children to commit to follow Christ.</p>
<p><strong>Kids Church:</strong><br>
Kids Church is designed to be a worship service for children. We offer musical worship, interactive large group games, teaching demonstrations, and media-enhanced Bible teaching. This is designed to be an introduction to church and hearing the Word of God taught. </p>
<p>Our weekend programming offers additional parent resources. There is a weekly family journal and memory verse for each unit. There is also an app available for family worship time: iPhones and iPads may download <a href="https://itunes.apple.com/us/app/gospel-project-for-kids-family/id561426629?mt=8">here</a>. Android users may download it <a href="https://play.google.com/store/apps/details?id=com.lifeway.tgpparents&amp;hl=en">here</a>. </p>
<p>Each weekend, your child(ren) will learn a part of the full Bible Gospel Story. To learn more about the curriculum, please watch this <a href="http://www.gospelproject.com/video/">video</a>. </p>
<p>Saturday Schedule:<br>
