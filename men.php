<h1 style="font-size: 19.5px;" class="post-title cufon_headings">Disciple-making Groups</h1>
<p>Disciple-making groups are groups of 3 to 4 men that meet on a weekly basis for up to a year. These groups provide accountability, transparency, and bible study truths to live by. These groups are an essential part part of the growth process for the believer. The curriculum, essentially a book of the Bible, is normally chosen by the leader of the group. The H.E.A.R. method is typically used to respond and record what God is doing in the believer’s life.</p>
<p>&nbsp;</p>
<h1 style="font-size: 19.5px;" class="post-title cufon_headings">Authentic Manhood</h1>
<p>More than just a rally or a Bible study, Authentic Manhood provides men with an encouraging process that teaches them how to live lives of authentic manhood as modeled by Jesus Christ and directed by the Word of God. It will help a man develop a compelling vision of Biblical masculinity that will bless both his life and the lives of those around him as he seeks to reject passivity, accept responsibility, and lead courageously.</p>
<h3 class="cufon_headings" style="font-size: 14.3px;">The Format</h3>
<p>The format is simple. We will meet at 6:30 PM on Wednesday evenings in the BX. There will be a teaching time followed by 30 minutes of small group interaction in an environment where men are free to talk openly and confidentially about what they hear, and express mutual support for one another.</p>
<h3 class="cufon_headings" style="font-size: 14.3px;">The Power</h3>
<p>The true power of Authentic Manhood lies in bringing men together on common ground to learn together and listen to each other. Bonds of true God-centered friendships are formed as men encourage each other week-by-week to more fully embrace a godly view of manhood. Our prayer is that the Men of Brainerd Baptist Church would rise up and take responsibility for their families and Church as they commit to pursue Biblical masculinity through participating in Men’s Fraternity.</p>
<p>&nbsp;</p>
<h1 style="font-size: 19.5px;" class="post-title cufon_headings">Adventures with Dad</h1>
<p>Adventures with Dad are special ministry opportunities for Dads and their children. The events are normally planned for a specific age group of children. Some of the annual opportunities include hiking, camping, rock-climbing, flag football, and others. Normally the events are low cost and pre-registration for the event is required.</p>
